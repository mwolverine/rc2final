import Foundation

class Session {
    
    let year: Int
    let month: Int
    let day: Int
    let hour: Int
    
    init(year: Int, month: Int, day: Int, hour: Int){
        self.year = year
        self.month = month
        self.day = day
        self.hour = hour
    }
    
}